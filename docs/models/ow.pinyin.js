function buildFilterText(text, isPinyin) {
  try {
    return isPinyin
      ? require("pinyin-pro")
          .pinyin(text, {
            toneType: "none",
            type: "array",
          })
          .join(" ")
      : text.split("").join(" ");
  } catch (error) {
    console.log(error);
  }
}
